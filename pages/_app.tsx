// _app.tsx
import { Global, css } from "@emotion/react";
import { RecoilRoot } from "recoil";
import Layout from "../src/components/commons/layout";
import "../styles/globals.css";
import Head from "next/head";
import { useRouter } from "next/router";
import { useEffect } from "react";

function MyApp({ Component, pageProps }) {
  const router = useRouter();
  const isResultPage = router.pathname === "/result";

  /* --------- Service Worker 등록 --------- */
  useEffect(() => {
    // CSR(Client-Side) 환경에서만 실행
    if ("serviceWorker" in navigator) {
      // 페이지 모든 리소스가 로드된 뒤 등록 -> 첫 진입 속도 저하 방지
      window.addEventListener("load", () => {
        navigator.serviceWorker
          .register("/sw.js") // ② public/sw.js → URL 루트 경로
          .then((reg) => {
            console.log("✅ Service Worker registered:", reg);
          })
          .catch((err) => {
            console.error("❌ Service Worker registration failed:", err);
          });
      });
    }
  }, []);
  /* ---------------------------------------- */

  const globalStyles = css`
    @font-face {
      font-family: "KBO Dia Gothic";
      src: url("/fonts/KBO-Dia-Gothic_medium.woff") format("woff");
      font-weight: normal;
      font-style: normal;
    }
    * {
      margin: 0;
      padding: 0;
      box-sizing: border-box;
    }

    html,
    body,
    #__next {
      font-family: "KBO-Dia-Gothic_medium";
      background-color: ${isResultPage ? "#f2f2f2" : "#ffffff"};
      margin: 0;
      padding: 0;
      /* height: 100vh; */
      /* height: calc(100% - 140px); */
    }

    /* 2) RootWebArea가 부모 높이(=100%)를 상속하도록 */
    .RootWebArea {
      /* height: 100%; */
      /* 또는 flex 레이아웃이라면 flex: 1; */
    }

    html,
    body,
    #__next,
    .RootWebArea {
      /* height: 100%; */
      margin: 0;
      padding: 0;
    }
  `;

  return (
    <>
      <Head>
        <title>SNU Baseball</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/icons/ball.png" />
      </Head>
      <Global styles={globalStyles} />
      <RecoilRoot>
        <Layout>
          <Component {...pageProps} />
        </Layout>
      </RecoilRoot>
    </>
  );
}

export default MyApp;
